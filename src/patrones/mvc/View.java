    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ClienteModel c = (ClienteModel) (jList1.getSelectedValue());
        int montoSolicitado = Integer.valueOf(jTextField5.getText());
        int plazo = Integer.valueOf(jTextField6.getText());
        try {
            controlador.getBanco().recibirSolicitudCredito(c, montoSolicitado, plazo);
            JOptionPane.showMessageDialog(this, "Solicitud Aprobada Exitosamente!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Solicitud Rechazada!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        ClienteModel c = (ClienteModel) (jList1.getSelectedValue());
        if (c != null) {
            jTextField1.setText(String.valueOf(c.getCedula()));
            jTextField2.setText(c.getNombres() + " " + c.getApellidos());
            jTextField3.setText(String.valueOf(c.getIngresos()));
            jTextField4.setText(String.valueOf(c.getMontoCredito()));
            jTextField5.setText("");
            jTextField6.setText("");
        } else {
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
        }
    }//GEN-LAST:event_jList1ValueChanged

